# ── Utility ───────────────────────────────────────────────────────────────────

def _html_to_text(html: str) -> str:
    """Very lightweight HTML → plain text for MCP responses."""
    if not html:
        return ""
    text = re_sub(r"<br\s*/?>", "\n", html, flags=0)
    text = re_sub(r"<p[^>]*>", "\n", text)
    text = re_sub(r"</p>", "\n", text)
    text = re_sub(r"<li[^>]*>", "\n  • ", text)
    text = re_sub(r"<h[1-6][^>]*>", "\n## ", text)
    text = re_sub(r"</h[1-6]>", "\n", text)
    text = re_sub(r"<th[^>]*>", " | ", text)
    text = re_sub(r"<td[^>]*>", " | ", text)
    text = re_sub(r"<[^>]+>", "", text)   # strip remaining tags
    text = unescape(text)
    # Collapse excess blank lines
    text = re_sub(r"\n{3,}", "\n\n", text)
    return text.strip()


def _format_ticket_summary(t: dict) -> str:
    """Format a ticket dict as a readable summary string."""
    ai_status = t.get("ai_status") or "not analysed"
    classification = t.get("ai_classification") or "—"
    partner = (
        (t.get("partner_id") or [None, "Unknown"])[1]
        if isinstance(t.get("partner_id"), list)
        else t.get("partner_name", "Unknown")
    )
    team = (
        (t.get("team_id") or [None, "—"])[1]
        if isinstance(t.get("team_id"), list)
        else "—"
    )
    stage = (
        (t.get("stage_id") or [None, "—"])[1]
        if isinstance(t.get("stage_id"), list)
        else "—"
    )
    priority_labels = {"0": "Normal", "1": "High", "2": "Very High", "3": "Critical"}
    priority = priority_labels.get(str(t.get("priority", "0")), "Normal")

    return (
        f"#{t.get('number', '?')}  {t.get('name', 'No title')}\n"
        f"  Partner: {partner}  |  Team: {team}  |  Stage: {stage}  |  Priority: {priority}\n"
        f"  AI: {ai_status}  |  Classification: {classification}"
        + (f"  |  Summary: {t.get('ai_summary', '')}" if t.get("ai_summary") else "")
    )


